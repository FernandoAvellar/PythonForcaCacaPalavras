import random


def jogar():
    imprime_mensagem_abertura()
    palavra_secreta = carrega_palavra_secreta()
    letras_acertadas = inicializa_letras_acertadas(palavra_secreta)
    print(letras_acertadas)

    enforcou = False
    acertou = False
    erros = 0

    while not enforcou and not acertou:
        chute = pede_chute_do_jogador()

        if chute in palavra_secreta:
            marca_chute_correto(chute, letras_acertadas, palavra_secreta)
        else:
            erros += 1
            desenha_forca(erros)

        enforcou = erros == 7
        acertou = "_" not in letras_acertadas
        print(letras_acertadas)

    if acertou:
        imprime_mensagem_vencedor()
    else:
        imprime_mensagem_perdedor(palavra_secreta)


def imprime_mensagem_abertura():
    print("*********************************")
    print("***Bem vindo ao jogo de Forca!***")
    print("*********************************")


def carrega_palavra_secreta():
    lista_de_palavras_formatada = []

    with open("palavras.txt", "r", 255,
              "UTF-8") as arquivo:  # melhor assim q já fecha automaticamente o arquivo em caso de erro
        for linha in arquivo:  # outra forma: arquivo = open("palavras.txt", "r", 255, "UTF-8") arquivo.close()
            lista_de_palavras_formatada.append(linha.strip().upper())

    return random.choice(lista_de_palavras_formatada)


def inicializa_letras_acertadas(palavra):
    return ["_" for letra in palavra]


def pede_chute_do_jogador():
    chute = input("Qual letra?")
    return chute.strip().upper()


def marca_chute_correto(chute, letras_acertadas, palavra_secreta):
    index = 0
    for letra in palavra_secreta:
        if chute == letra:
            letras_acertadas[index] = letra
        index += 1


